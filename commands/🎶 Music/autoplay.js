const Discord = require("discord.js");
const { MessageEmbed } = require("discord.js");
const config = require("../../botconfig/config.json");
const ee = require("../../botconfig/embed.json");
const playermanager = require("../../handlers/playermanager");
module.exports = {
    name: "autoplay",
    category: "üé∂ Music",
    aliases: ["ap", "toggleauto", "toggleautoplay", "toggleap"],
    description: "Toggles Autoplay on/off",
    usage: "autoplay",
    run: async (client, message, args, cmduser, text, prefix) => {
    try{
      //get the channel instance
      const { channel } = message.member.voice;
      //if not in a voice Channel return error
      if (!channel)
          return message.channel.send(new MessageEmbed()
              .setColor(ee.wrongcolor)
              .setFooter(ee.footertext, ee.footericon)
              .setTitle("‚ùå Error | You need to join a voice channel.")
          );
      //get the player instance
      const player = client.manager.players.get(message.guild.id);
      //if no player available return error | aka not playing anything
      if (!player)
        return message.channel.send(new MessageEmbed()
          .setColor(ee.wrongcolor)
          .setFooter(client.user.username, ee.footericon)
          .setTitle("‚ùå Error | There is nothing playing")
        );
      //if not in the same channel --> return
      if(player && channel.id !== player.voiceChannel)
        return message.channel.send(new MessageEmbed()
          .setColor(ee.wrongcolor)
          .setFooter(ee.footertext, ee.footericon)
          .setTitle("‚ùå Error | You need to be in my voice channel to use this command!")
          .setDescription(`Channelname: \`${message.guild.channels.cache.get(player.voiceChannel).name}\``)
        );
        //toggle autoplay
        player.set("autoplay", !player.get("autoplay"))
        //Send Success Message
        return message.channel.send(new MessageEmbed()
          .setTitle(`‚úÖ Success | ${player.get("autoplay") ? `\`‚úîÔ∏è Enabled\`` : `\`‚ùå Disabled\``} Autoplay`)
          .setDescription(`To ${player.get("autoplay") ? "disable" : "enable" } it type: \`${prefix}autoplay\``)
          .setColor(ee.color)
          .setFooter(ee.footertext, ee.footericon)
        );
    } catch (e) {
        console.log(String(e.stack).bgRed)
        return message.channel.send(new MessageEmbed()
            .setColor(ee.wrongcolor)
						.setFooter(ee.footertext, ee.footericon)
            .setTitle(`‚ùå ERROR | An error occurred`)
            .setDescription(`\`\`\`${e.stack}\`\`\``)
        );
    }
  }
};
